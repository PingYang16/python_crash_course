# use the range() function
for value in range(1,5):
    print(value)
# prints only the numbers 1 through 4. Stop at the second value you provide.

# to print from 1 to 5,
for value in range(1,6):
    print(value)

# can also pass range() only one argument, start from 0
for value in range(6):
    print(value)